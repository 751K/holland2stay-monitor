def tr(key: str, lang: str) -> str:
    """Look up a translation key. Falls back to zh if key missing."""
    entry = TRANSLATIONS.get(key)
    if not entry:
        logger.warning("Missing translation key: %s", key)
        return key
    return entry.get(lang, entry.get("zh", key))
